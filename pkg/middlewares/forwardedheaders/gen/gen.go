package main

import (
	"bytes"
	"fmt"
	"go/format"
	"log"
	"net/http"
	"os"
	"sort"

	"github.com/traefik/traefik/v2/pkg/middlewares/forwardedheaders/xheaders"
)

func main() {
	var buf bytes.Buffer
	fmt.Fprintln(&buf, "// Code generated by gen.go; DO NOT EDIT.")
	fmt.Fprintln(&buf)
	fmt.Fprintln(&buf, "package forwardedheaders")
	fmt.Fprintln(&buf)
	fmt.Fprintln(&buf, "// xHeadersMap contains all canonical X-Forwarded header keys managed by Traefik,")
	fmt.Fprintln(&buf, "// including their underscore variants as canonicalized by Go's HTTP server.")
	fmt.Fprintln(&buf, "var xHeadersMap = map[string]struct{}{")
	for _, h := range xheaders.Headers {
		for _, v := range underscoreVariants(h) {
			fmt.Fprintf(&buf, "\t%q: {},\n", v)
		}
	}
	fmt.Fprintln(&buf, "}")

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		log.Fatalf("formatting generated code: %v", err)
	}

	if err := os.WriteFile("pkg/middlewares/forwardedheaders/headers.go", formatted, 0o644); err != nil {
		log.Fatalf("writing headers.go: %v", err)
	}
}

// underscoreVariants generates all variants of a header where each '-'  can be replaced by '_',
// and returns them canonicalized as Go's HTTP server would (http.CanonicalHeaderKey).
func underscoreVariants(header string) []string {
	var dashPositions []int
	for i, c := range header {
		if c == '-' {
			dashPositions = append(dashPositions, i)
		}
	}

	n := len(dashPositions)
	variants := make(map[string]struct{})

	// Generate all 2^n combinations of dash/underscore.
	for mask := range 1 << n {
		b := []byte(header)
		for bit, pos := range dashPositions {
			if mask&(1<<bit) != 0 {
				b[pos] = '_'
			}
		}
		canonical := http.CanonicalHeaderKey(string(b))
		variants[canonical] = struct{}{}
	}

	result := make([]string, 0, len(variants))
	for v := range variants {
		result = append(result, v)
	}

	sort.Strings(result)

	return result
}
